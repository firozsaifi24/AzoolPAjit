package com.example.firozsaifi.azoolp.model;

public class Shop {

    private String id;
    private String name;
    private String companyName;
    private String companyNameArabic;
    private String image;
    private String totalProducts;
    private String sparePart;
    private String accidentCar;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getCompanyNameArabic() {
        return companyNameArabic;
    }

    public void setCompanyNameArabic(String companyNameArabic) {
        this.companyNameArabic = companyNameArabic;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getTotalProducts() {
        return totalProducts;
    }

    public void setTotalProducts(String totalProducts) {
        this.totalProducts = totalProducts;
    }

    public String getSparePart() {
        return sparePart;
    }

    public void setSparePart(String sparePart) {
        this.sparePart = sparePart;
    }

    public String getAccidentCar() {
        return accidentCar;
    }

    public void setAccidentCar(String accidentCar) {
        this.accidentCar = accidentCar;
    }
}
